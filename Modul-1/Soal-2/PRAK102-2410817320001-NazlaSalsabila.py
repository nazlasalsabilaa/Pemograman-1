# Mendefinisikan variabel
a = 4
b = 8
c = 3

# Menampilkan nilai variabel
print("Variabel a bernilai", a)
print("Variabel b bernilai", b)
print("Variabel c bernilai", c)

# Menghitung hasil a dikali b dibagi c
hasil = (a * b) / c

# Menampilkan hasil dengan 6 angka di belakang koma
print("Hasil dari a dikali b dibagi c adalah", format(hasil, '.6f'))