# Mendefinisikan variabel
a = 9
b = 6
x = 10
y = 7

# Menampilkan nilai variabel
print("Variabel a bernilai", a)
print("Variabel b bernilai", b)
print("Variabel x bernilai", x)
print("Variabel y bernilai", y)

# Menghitung hasil dari ((a + b) dikali x) dan kemudian dibagi y
hasil = ((a + b) * x) / y

# Menampilkan hasil dengan 2 angka di belakang koma
print("Hasil dari a ditambah b dikali x dan dibagi y adalah", format(hasil, '.2f'))