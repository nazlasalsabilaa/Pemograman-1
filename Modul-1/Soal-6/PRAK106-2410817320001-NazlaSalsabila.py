# Mendefinisikan variabel
a = 4
b = 8
c = 3

# Menampilkan nilai variabel
print("Variabel a bernilai", a)
print("Variabel b bernilai", b)
print("Variabel c bernilai", c)

# Pengecekan apakah a sama dengan b (menggunakan ==)
hasil_sama = a == b

# Pengecekan apakah b lebih besar dari c (menggunakan >)
hasil_lebih_besar = b > c

# Pengecekan apakah a tidak sama dengan c (menggunakan !=)
hasil_tidak_sama = a != c

# Menampilkan hasil pengecekan
print("Apakah a sama dengan b ? jawabannya adalah", int(hasil_sama))
print("Apakah b lebih besar dari c ? jawabannya adalah", int(hasil_lebih_besar))
print("Apakah a tidak sama dengan c ? jawabannya adalah", int(hasil_tidak_sama))
