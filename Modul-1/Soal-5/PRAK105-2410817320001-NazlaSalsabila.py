# Mendefinisikan variabel
a = 9
b = 5
x = 8
y = 8

# Menampilkan nilai variabel
print("Variabel a bernilai", a)
print("Variabel b bernilai", b)
print("Variabel x bernilai", x)
print("Variabel y bernilai", y)

# Menghitung sisa bagi dari a dibagi b dan x dibagi y
sisa_a_b = a % b
sisa_x_y = x % y

# Menghitung total sisa bagi
total_sisa_bagi = sisa_a_b + sisa_x_y

# Menampilkan total sisa bagi
print("Total sisa bagi dari a dibagi b dan x dibagi y adalah", total_sisa_bagi)